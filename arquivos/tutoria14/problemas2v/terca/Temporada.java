public class Temporada {
    private Episodio[] episodios;

    public Temporada(Episodio[] episodios) {
        this.episodios = episodios;
    }

    public Episodio[] getEpisodios() {
        return episodios;
    }

    public void setEpisodios(Episodio[] episodios) {
        this.episodios = episodios;
    }
}
